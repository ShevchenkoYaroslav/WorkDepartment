public class Departaments {

    private String nameOfDepartament;
    private String siteOfOfDepartament;
    private String phoneOfDepartament;
    private int numOfWorkers;
    private long idOfDepartament;

    public String getNameOfDepartament() {
        return nameOfDepartament;
    }

    public void setNameOfDepartament(String nameOfDepartament) {
        this.nameOfDepartament = nameOfDepartament;
    }

    public String getSiteOfOfDepartament() {
        return siteOfOfDepartament;
    }

    public void setSiteOfOfDepartament(String siteOfOfDepartament) {
        this.siteOfOfDepartament = siteOfOfDepartament;
    }

    public String getPhoneOfDepartament() {
        return phoneOfDepartament;
    }

    public void setPhoneOfDepartament(String phoneOfDepartament) {
        this.phoneOfDepartament = phoneOfDepartament;
    }

    public int getNumOfWorkers() {
        return numOfWorkers;
    }

    public void setNumOfWorkers(int numOfWorkers) {
        this.numOfWorkers = numOfWorkers;
    }

    public long getIdOfDepartament() {
        return idOfDepartament;
    }

    public void setIdOfDepartament(long idOfDepartament) {
        this.idOfDepartament = idOfDepartament;
    }
}
